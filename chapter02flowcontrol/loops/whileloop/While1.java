package chapter02flowcontrol.loops.whileloop;

// Program to display numbers from 1 to 5
class While1 {
    public static void main(String[] args) {
        // declare variables
        int i = 0, n = 5;

        // while loop from 1 to 5
        while (i < n) {
            i++;
            System.out.println(i);
        }

    }
}
