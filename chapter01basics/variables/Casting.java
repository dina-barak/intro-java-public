package chapter01basics.variables;

public class Casting {
    public static void main(String[] args) {
        double a = 17; // a = 17.0
        double b = 5 / 2; // b = 2.0
        double s = (double)5 / 2; // b = 2.5
        int x = (int) 15.7; // x = 15
        int y = (int) 'e'; // y = 101
        char c = (char) 101; // c = ‘e’
    }
}
